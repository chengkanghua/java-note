public class Demo02 {
    public static void main(String[] args) {
        // 常用數據類型
        int i = 10000; // 整數
        double d = 3.14; // 小數
        char c = '中'; // 字符
        String s = "中國"; // 字符串
        System.out.println("整數：" + i + "，小數：" + d + "，字符：" + c + "，字符串：" + s);
    }
}
