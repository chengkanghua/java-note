/**
 * 文档注释
 */
public class Demo01 {
    /*
    多行注释
     */
    public static void main(String[] args) {
        // 单行注释
        System.out.print("Hello World!\n"); // ctrl+d复制整行
        System.out.print("Hello World!\n"); // \n换行符
        System.out.print("Hello World!\n");
        System.out.println("a\tabc"); // \t制表符
        System.out.println("ab\tabc"); // \t制表符
        System.out.println("abc\tabc"); // \t制表符
    }
}
